/**
 * Look up the CEFR level for a German word.
 * Returns undefined if the word is not in the map.
 */
export function lookupCefrLevel(word: string): string | undefined {
  const normalised = word
    .toLowerCase()
    .replace(/^(der|die|das|ein|eine)\s+/, "");
  return CEFR_MAP[normalised];
}

/**
 * Correct an AI-assigned CEFR level using the lookup table.
 * Falls back to the AI level if the word is not in the map.
 */
export function correctLevel(word: string, aiLevel: string): string {
  return lookupCefrLevel(word) ?? aiLevel;
}
