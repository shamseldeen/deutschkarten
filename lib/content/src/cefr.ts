/**
 * Look up the CEFR level for a German word.
 * Returns undefined if the word is not in the map.
 */
export function lookupCefrLevel(word: string): string | undefined {
  const normalised = word
    .toLowerCase()
    .replace(/^(der|die|das|ein|eine)\s+/, "");
  return CEFR_MAP[normalised];
}

/**
 * Look up the Part-of-Speech for a German word.
 * Returns undefined if the word is not in the map.
 */
export function lookupPos(word: string): PartOfSpeech | undefined {
  const normalised = word
    .toLowerCase()
    .replace(/^(der|die|das|ein|eine)\s+/, "");
  return POS_MAP[normalised];
}

/**
 * Look up both CEFR level and POS for a German word in one call.
 * Returns an object with whichever fields are known; both may be undefined
 * when the word is not in the maps.
 */
export function lookupWord(word: string): {
  level: string | undefined;
  pos: PartOfSpeech | undefined;
} {
  const normalised = word
    .toLowerCase()
    .replace(/^(der|die|das|ein|eine)\s+/, "");
  return { level: CEFR_MAP[normalised], pos: POS_MAP[normalised] };
}

/**
 * Correct an AI-assigned CEFR level using the lookup table.
 * Falls back to the AI level if the word is not in the map.
 */
export function correctLevel(word: string, aiLevel: string): string {
  return lookupCefrLevel(word) ?? aiLevel;
}

